'''
007 - Desenvolva um programa que leia as duas notas de um aluno, calcule e mostre a sua média aritimética.
'''

print("Cálculo da Média")

user_grade_1 = float(input('Digite a primeira nota:\n'))
user_grade_2 = float(input('Digite a segunda nota:\n'))

user_average = (user_grade_1 + user_grade_2) / 2

print(f'A média final é: {user_average}')

